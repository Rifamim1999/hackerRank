def designerPdfViewer(h, word):
    # Write your code here
    letters =['a','b','c','d','e','f','g','h','i','j','k','l','m','n','o','p','q','r','s','t','u','v','w','x','y','z']
    li=[]
    for i in range(len(letters)):
        for j in range(len(word)):
            if word[j]==letters[i]:
                li.append(h[i])
    li.sort(reverse = True)
    maxi=li[0]
    a = maxi*len(word)
    return a

